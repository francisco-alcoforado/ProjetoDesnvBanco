    public final void montarTabela(){
        DefaultTableModel model;
            model = (DefaultTableModel) this.TabelaClientes.getModel();
        try {
            Fachada fachada = new Fachada();
            ArrayList<Cliente> lista = fachada.listarCliente();
            this.clientes = lista;
            int i = 0;
            for(Cliente cliente : lista){
                String email = "";
                String telefone = "";
                if(cliente.getEmails().size() > 0){
                	email = cliente.getEmails().get(0).getEmail(); 
                }
                if(cliente.getTelefones().size() > 0){
                	telefone = cliente.getTelefones().get(0).getTelefone();
                }
                model.addRow(new Object[]{cliente.getCodigo(), cliente.getNome(), email, cliente.getRua(), cliente.getNumero(), cliente.getComplemento(), cliente.getBairro(), cliente.getCidade(), cliente.getCEP(), telefone});
                i++;
            }
            TabelaClientes.setVisible(true);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ListarClientes.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(ListarClientes.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(ListarClientes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
