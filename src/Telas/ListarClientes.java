        int codigo = (int) TabelaClientes.getValueAt(row, 0);
        Cliente choosen = null;
        for(Cliente cliente : this.clientes){
            if(cliente.getCodigo() == codigo){
                choosen = cliente;
                break;
            }
        }
        FormularioCliente form = new FormularioCliente(choosen);
        Principal.setAreaTrabalhoPrincipal(form);
        form.setVisible(true);
