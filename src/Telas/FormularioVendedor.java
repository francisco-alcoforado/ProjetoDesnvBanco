    private void btnCadastrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCadastrarMouseClicked
        // TODO add your handling code here:
        boolean resposta = this.validar();
        if(resposta == false){
            Erros.setText("");
            Erros.setText("Campo(s) obrigatorio(s) mal preenchido!!!");
            Erros.setVisible(true);
            
        }
        String nome = this.nome.getText();
        String CPF = this.CPF.getText();
        String rua = this.rua.getText();
        int numero = Integer.parseInt(this.numero.getText());
        String complemento = this.complemento.getText();
        String bairro = this.bairro.getText();
        String cidade = this.cidade.getText();
        String CEP = this.CEP.getText();
        String email = this.email.getText();
        String telefone = this.telefone.getText();
        String senha = String.valueOf(this.senha.getPassword());
        ArrayList<EmailVendedor> emails = new ArrayList<EmailVendedor>();
        emails.add(new EmailVendedor(1, email, 1));
        ArrayList<TelefoneVendedor> telefones = new ArrayList<TelefoneVendedor>();
        telefones.add(new TelefoneVendedor(1, telefone));
        Vendedor vendedor = new Vendedor(1, nome, senha, CPF, rua, bairro, cidade, CEP, numero, complemento, telefones, emails);
        Fachada fachada = new Fachada();
        try {
            fachada.cadastrarVendedor(vendedor);
        } catch (IllegalArgumentException ex) {
            Logger.getLogger(FormularioVendedor.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(FormularioVendedor.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(FormularioVendedor.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(FormularioVendedor.class.getName()).log(Level.SEVERE, null, ex);
        } catch (VendedorJaCadastradoException ex) {
            Logger.getLogger(FormularioVendedor.class.getName()).log(Level.SEVERE, null, ex);
        } catch (VendedorNaoEncontradoException ex) {
            Logger.getLogger(FormularioVendedor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnCadastrarMouseClicked

