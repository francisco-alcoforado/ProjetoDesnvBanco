    private boolean confirmarSenha(){
        String senha = String.valueOf(this.senha.getPassword());
        String confirm = String.valueOf(this.confirnarSenha.getPassword());
        if(senha != confirm){
            return false;
        }
        
        if(this.validarTexto(senha) == false)
            return false;
        if(senha.indexOf("'") != -1)
            return false;
        if(senha.indexOf("\"") != -1)
            return false;
        if(senha.indexOf("/") != -1)
            return false;
        if(senha.indexOf("\\") != -1)
            return false;
        if(senha.indexOf("^") != -1)
            return false;
        if(senha.indexOf("´") != -1)
            return false;
        if(senha.indexOf(" ") != -1)
            return false;
        if(senha.indexOf(",") != -1)
            return false;
        
        return true;
    }
    private boolean validar(){
        if(this.confirmarSenha() == false){
            return false;
        }
        
        String[] valores = new String[]{nome.getText(), email.getText(), this.prepararCPF(CPF.getText()), this.prepararCEP(CEP.getText()), rua.getText(), complemento.getText(), cidade.getText(), bairro.getText(), this.prepararTelefone(telefone.getText())};
        boolean valido = this.validarTexto(nome.getText());
        int i = 0;
        for(String valor : valores){
            if(this.validarTexto(valor) == false){
                return false;
            }
            if(i == 1){
                if(this.validarEmail(valor) == false){
                  return false;   
                }
            }
            i++;
        }
        return true;
    }
    private String prepararTelefone(String telefone){
        String valor = telefone.replace(".", "");
        valor = telefone.replace("(", "");
        valor = telefone.replace(")", "");
        valor = telefone.replace("-", "");
        return valor;
    }
    private String prepararCPF(String CPF){
        String valor = CPF.replace(".", "");
        valor = CPF.replace("-", "");
        return valor;
    }
    private String prepararCEP(String CEP){
        String valor = CEP.replace(".", "");
        valor = CEP.replace("-", "");
        return valor;
    }
    private boolean validarEmail(String email){
        int posicao = email.indexOf("@");
        if(posicao == -1){
            return false;
        }
        String parte = email.substring(posicao);
        if(parte.indexOf(".") == -1){
            return false;
        }
        return true;
    }
    private boolean validarTexto(String valor){
        if(valor.isEmpty()){
            return false;
        }
        
        if(valor.trim().equals("")){
            return false;
        }
        
        String[] valores = new String[]{"*", "_", "'", "=", "+", "§", "{", "}", "[", "]", "?", ":", ";", "?", "|", "\\", "/", ","};
        int correta = 0;
        
        for(String caractere : valores){
            if(valor.indexOf(caractere) != -1){
                return false;
            }
            if(this.quantOcorrencia(caractere, valor) == valor.length()){
                return false;
            }
        } 
        return true;
    }
    
    private int quantOcorrencia(String needle, String stack){
        String[] valores = stack.split("");
        int quantOcorrencias = 0;
        for(String valor : valores){
           if(valor.equals(needle)){
               quantOcorrencias++;
           }
        }
        return quantOcorrencias;
    }
